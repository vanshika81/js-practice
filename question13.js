//1. Write a function that takes a name and age, and returns a string using template literals
// : "My name is Alice and I am 25 years old."

function templateLiteral(name, age) {
  return `My name is ${name} and I am ${age} years old.`;
}
console.log(templateLiteral("vanshika",31)) //o/p:  My name is vanshika and I am 31 years old.