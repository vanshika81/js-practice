if (true) {
    var var1 = "welcome to Javascript";
    console.log("Inside the block:", var1);
}

console.log("Outside the block:", var1);


// Using let
if (true) {
    let var2 = "welcome to javascript";
    console.log("Inside the block :", var2);
}
console.log("Outside the block:", var2);

